package javaprogrammes;

/**
 * Java program to print the sum(addition), multiply, subtract, divide and remainder of two numbers.
 * Test Data is given.
 */

public class MultiOperations18 {
    //Main method
    public static void main(String[] args) {
        //1. Numeric Test Data
        int first = 125;
        int second = 24;

        //print statement
        System.out.println("");
        System.out.print("125 + 24 =");
        System.out.println(first + second);
        System.out.println("");
        System.out.print("125 - 24 =");
        System.out.println(first - second);
        System.out.println("");
        System.out.print("125 * 24 =");
        System.out.println(first * second);
        System.out.println("");
        System.out.print("125 / 24 =");
        System.out.println(first / second);
        System.out.println("");
        System.out.print("125 mod 24 =");
        System.out.println(first / second);

    }


}
